package de.tub.model;

public class Metainfo {
    int varinace;
    int delay;
    int threads;


    public int getVarinace() {
        return varinace;
    }

    public void setVarinace(int varinace) {
        this.varinace = varinace;
    }

    public int getDelay() {
        return delay;
    }

    public void setDelay(int delay) {
        this.delay = delay;
    }

    public int getThreads() {
        return threads;
    }

    public void setThreads(int threads) {
        this.threads = threads;
    }
}
